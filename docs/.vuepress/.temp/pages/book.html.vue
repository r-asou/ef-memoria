<p><strong>06年08年まで、雑誌にて連載されたショートストーリー「ef - a fairy tale of the two.- another tale」。それらをまとめたムックが「ANOTHER TALES.(アナザー テイルズ)」です。</strong></p>
<p><strong>「コンプティーク(角川書店・刊)」誌にて掲載された「ef - a fairy tale of the two.～ another tale」は、ゲーム本編には描かれない、番外編的な物語や、登場人物達の活き活きとしたやりとりをお楽しみいただけるショートストーリーをメインに、カラー描き下ろし挿絵、描き下ろし四コマ漫画で構成された連載。スタートより2008年7月の完結まで約2年にわたり、掲載されました。</strong></p>
